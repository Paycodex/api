    /**
     * 
     * @throws comcodex.ServiceClientException
     */
    public void filterTransactionList() throws ServiceClientException
    {
        try
        {
            SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
            
            TransactionQuery query = new TransactionQuery();
            
            if( !"".equals(this.textboxBeginDate.getText().trim()) )
                query.beginDate = formatter.parse(this.textboxBeginDate.getText().trim());
            
            if( !"".equals(this.textboxEndDate.getText().trim()) )
                query.endDate = formatter.parse(this.textboxEndDate.getText().trim());
            
           
            Transaction[] transactions = this.serviceClient.listTransactions(query);
            
            TransactionListBoxModel model = new TransactionListBoxModel();
            model.fill(transactions);
            this.listboxTransactions.setModel(model);
            
        }
        catch(Exception ex)
        {
            //"Error en el filtro, revise las fechas, deben ser dd/mm/yyy"
            //this.showError(ex.getMessage());
            throw new ServiceClientException("Error en el filtro, revise las fechas, deben ser dd/mm/yyy");
        }
    }
    
    public void revertTransaction() throws ServiceClientException
    {
        
        try{
            
            if (this.listboxTransactions.getSelectedIndex() >= 0) {
               
                TransactionDisplayItem transactionDisplayItemSelected = (TransactionDisplayItem) this.listboxTransactions.getSelectedValue();
                Transaction transaction = transactionDisplayItemSelected.getValue();
                
                if (transaction != null) {
                    
                    if(transaction.status == comcodex.Transaction.STATUS_OK){
                        
                        Transaction transactionClient = this.serviceClient.revertTransaction(transaction); 
                        
                        if(transactionClient.status == comcodex.Transaction.STATUS_REVERT){
                            
                            JOptionPane.showMessageDialog(this, "La transacción fue revertida exitosamente.");
                            this.labelTitleStatusValue.setText( this.getLabelStatus( transaction.status ) );
                            
                            // Actualizar listado
                            DialogLoading loading = this.buildLoading();
                            
                            CustomWorker worker = new CustomWorker(CustomWorker.FILTER_TRANSACTION_LIST, this, loading);
                            
                            worker.execute();
                            
                            loading.setVisible(true);
                            
                        }else{
                            
                            this.showError("La transacción no pudo ser revertida.");
                        }
                        
                    }else{
                        
                        this.showError("La transacción debe estar pagada.");
                    }

                }

            }
            
            
            //
            
        }catch(Exception ex){
            throw new ServiceClientException("Error al intentar revertir la operacion indicada.");
        }
        
    }
    
