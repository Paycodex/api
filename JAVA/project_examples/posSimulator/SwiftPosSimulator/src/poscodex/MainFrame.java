    /**
     * 
     * @param status
     * @return 
     */
    public String getLabelStatus(int status)
    {
        String result = "En espera";
        
        switch(status)
        {
            case Transaction.STATUS_REJECTED:
                result = "Rechazada";
                break;   
            case Transaction.STATUS_FAIL:
                result = "Falla Servicio";
                break;   
            case Transaction.STATUS_OK:
                result = "Pagada";
                break; 
            case Transaction.STATUS_REVERT:
                result = "Revertida";
                break;
        }
      
        return result;
    }
    
    /**
     * 
     */
    public void showTransaction() throws ServiceClientException
    {
        if( !"".equals(this.currentToken) )
        {
            Transaction transaction;

            transaction = this.serviceClient.retrieveTransaction(this.currentToken);

            try
            {

                this.labelTitleAmountValue.setText( transaction.amount.number.toString() + "." + transaction.amount.decimal.toString() );
                this.labelTitleDateValue.setText(transaction.openDate.toString() );
                this.labelTitleDeviceValue.setText(transaction.device );
                this.labelTitleConceptValue.setText(transaction.concept );
                
                this.labelTitleStatusValue.setText(     this.getLabelStatus( transaction.status ) );
                
                if( transaction.payed )
                {
                    
                    this.labelTitlePayerValue.setText(          transaction.payedCardHolder );
                    this.labelTitleCardNumberValue.setText(     transaction.payedCardNumber.substring( transaction.payedCardNumber.length()-8 ).toUpperCase() );
                    this.labelTitlePayerIdentityValue.setText(  transaction.payedIdentity );
                    this.labelTitlePayDateValue.setText(        transaction.payedDate.toString() );     
                }
                else
                {
                    this.labelTitlePayerValue.setText( "" );
                    this.labelTitleCardNumberValue.setText( "" );
                    this.labelTitlePayerIdentityValue.setText( "" );
                    this.labelTitlePayDateValue.setText( "" );
                }

                String imagePath = this.serviceClient.getQrImage(transaction);
                ImageIcon imageIcon = new ImageIcon(imagePath);

                Graphics g = this.panelQr.getGraphics();
                g.clearRect(0, 0, this.panelQr.getWidth(), this.panelQr.getHeight());
                g.drawImage( imageIcon.getImage(), 0, 0, Color.WHITE, null );
            } 
            catch (Exception ex) 
            {
                 this.showError("Error showTransaction ", ex.getMessage() );
            }

        }

    }
    
    
