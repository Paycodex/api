    public void loadTransactionList()
    {
        
            TransactionQuery query = new TransactionQuery();
            
            try 
            {
                Transaction[] transactions = this.serviceClient.listTransactions(query );
                
                TransactionListBoxModel model = new TransactionListBoxModel();
                model.fill(transactions);
                
                this.listboxTransactions.setModel(model);
                
            } 
            catch (ServiceClientException ex) 
            {
                 this.showError("Error obteniendo las lista de transacciones del perfil.", ex.getMessage() );
            }
    }
    
